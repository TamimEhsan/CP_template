/*
    Euler totient function
    Euler's totient function, also known as phi-function phi(n),
    counts the number of integers between 1 and n inclusive,
    which are coprime to n.
    calculate value from 1 to N
    time:   N = 10^7    0.511 s
            N = 10^8    5.8 s
*/


void phi_1_to_n(int n) {
    vector<int> phi(n + 1);
    phi[0] = 0;
    phi[1] = 1;
    for (int i = 2; i <= n; i++)
        phi[i] = i;

    for (int i = 2; i <= n; i++) {
        if (phi[i] == i) {
            for (int j = i; j <= n; j += i)
                phi[j] -= phi[j] / i;
        }
    }
}
